// indirectly 2D gradients via weights
// can be made into arrays that match T_TERRAINS size

cl_rgb getStoneColor(int terrain)
{
	switch (terrain)
	{
	case T_ICECAP:
	case T_SNOW:    // 
		return cl_rgb(126, 126, 126);
	case T_AUTUMN:  // 
		return cl_rgb(126, 126, 126);
	case T_ISLANDS: // 
		return cl_rgb(126, 126, 126);
	case T_GRASS:   // 
		return cl_rgb(126, 126, 126);
	case T_MARSH:
	case T_JUNGLE:  // 
		return cl_rgb(126, 126, 126);
	case T_DESERT:  // 
		return cl_rgb(121, 96, 90);
	}
	return cl_rgb(255, 0, 255);
}
